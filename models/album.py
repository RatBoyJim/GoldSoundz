class Album:

    def __init__(self, id, title, artist, label, amount_units, ideal_units, cost, sell_price, genre, label_id):
        self.id = id
        self.title = title
        self.artist = artist
        self.label = label
        self.amount_units = amount_units
        self.ideal_units = ideal_units
        self.cost = cost
        self.sell_price = sell_price
        self.genre = genre
        self.label_id = label_id